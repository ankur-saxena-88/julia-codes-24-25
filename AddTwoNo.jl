# Julia program to add two numbers using function

function addTwoNo()

    num1 = 20
    num2 = 30

    sum = num1 + num2

    println("First number is $num1")
    println("Second number is $num2")
    println("The sum of $num1 and $num2 is $sum")
end

# call function
println(addTwoNo())
